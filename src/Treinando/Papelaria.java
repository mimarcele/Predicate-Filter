package Treinando;

public class Papelaria {

    final String objeto;
    final double preco;
    final boolean estaNaLista;

    public Papelaria(String objeto, double preco, boolean estaNaLista) {
        this.objeto = objeto;
        this.preco = preco;
        this.estaNaLista = estaNaLista;
    }
}
